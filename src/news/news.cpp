			issues.push_back(VIEW_FREESPEECH);
			issues.push_back(VIEW_GAY);
			issues.push_back(VIEW_WOMEN);
			issues.push_back(VIEW_CIVILRIGHTS);
			break;
		case SITE_GOVERNMENT_PRISON:
